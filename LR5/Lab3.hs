module Lab3 where
reverseWithout :: [a] -> [a]
reverseWithout [] = []
reverseWithout [x] = [x]
reverseWithout (x:xs) = reverseWithout xs++ [x]

-- Вхідні дані:
-- ghci> reverseWithout "r32rfea"
-- Вихідні дані:
-- "aefr23r"


