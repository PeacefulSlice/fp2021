-- Завдання 2.Перемішування списку з n елементів: після першого розмістити останній,
-- після другого передостанній тощо. 

--2.1)
mixWithout :: [a] -> [a]
mixWithout [] = []
mixWithout [x] = [x]
mixWithout (x:xs) = [x] ++ mixWithout(reverseWithout xs)

-- Вхідні дані:
-- ghci> mix_without "123456789" 
-- Вихідні дані:
-- "192837465"

--2.2)
mixWith :: [a] -> [a]
mixWith [] = []
mixWith [x] = [x] 
mixWith (x:xs) = [x] ++ mixWith(Prelude.reverse xs)  


-- Вхідні дані:
-- ghci> mixWith "123456789" 
-- Вихідні дані:
-- "192837465"

